    public static string T(HttpContext context, string key, string? ru = null)
    {
        var lang = context.Request.Cookies["lang"];
        var dictionary = lang == "en" ? En : Ru;
        if (dictionary.TryGetValue(key, out var value))
        {
            return value;
        }

        if (Ru.TryGetValue(key, out var ruValue))
        {
            return lang == "en" ? key : ruValue;
        }

        return ru ?? key;
    }

    public static string Status(HttpContext context, string status)
    {
        var lang = context.Request.Cookies["lang"];

        return status switch
        {
            Models.TicketStatuses.New => lang == "en" ? "New" : "Новый",
            Models.TicketStatuses.BotAnswered => lang == "en" ? "Bot answered" : "Ответ бота",
            Models.TicketStatuses.WaitingForOperator => lang == "en" ? "Waiting for operator" : "Ждёт оператора",
            Models.TicketStatuses.InProgress => lang == "en" ? "In progress" : "В работе",
            Models.TicketStatuses.Closed => lang == "en" ? "Closed" : "Закрыт",
            _ => status
        };
    }
